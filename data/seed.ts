// Helper for ZhongTe
const createZhongTe = (id: number, content: string, arg3: any, arg4?: any, mnemonic?: string): Question => {
  // Judge
  if (typeof arg3 === 'boolean') {
      return {
          id: `zhongte_${id}`,
          type: QuestionType.JUDGE,
          content,
          correctAnswer: arg3,
          category: '中特',
          mnemonic: mnemonic || undefined,
          tags: ['中特', '期末题库', '判断题']
      };
  }
  // Single Choice (options[], answer)
  if (Array.isArray(arg3) && typeof arg4 === 'string') {
      return {
          id: `zhongte_${id}`,
          type: QuestionType.CHOICE,
          content,
          options: arg3,
          correctAnswer: [arg4],
          category: '中特',
          mnemonic: mnemonic || undefined,
          tags: ['中特', '期末题库', '单选题']
      };
  }
  // Multi Choice (answer[], options[])
  if (Array.isArray(arg3) && Array.isArray(arg4)) {
      return {
          id: `zhongte_${id}`,
          type: QuestionType.CHOICE,
          content,
          options: arg4, 
          correctAnswer: arg3, 
          category: '中特',
          mnemonic: mnemonic || undefined,
          tags: ['中特', '期末题库', '多选题']
      };
  }
  // Simplified Single Choice (answer, options[]) for 1-150 parsing
  return {
    id: `zhongte_${id}`,
    type: QuestionType.CHOICE,
    content,
    options: Array.isArray(arg4) ? arg4 : undefined,
    correctAnswer: [arg3],
    category: '中特',
    tags: ['中特', '期末题库', '单选题']
  };
};

