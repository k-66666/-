// Helper to quickly create the ZhongTe questions
const createZhongTe = (id: number, content: string, arg3: any, arg4?: any): Question => {
  // 1. Judge (boolean) passed as 3rd arg
  if (typeof arg3 === 'boolean') {
      return {
          id: `zhongte_${id}`,
          type: QuestionType.JUDGE,
          content,
          correctAnswer: arg3,
          category: '中特',
          tags: ['中特', '期末题库']
      };
  }

  // 2. 1-150 Style: (options: string[], answer: string)
  if (Array.isArray(arg3) && typeof arg4 === 'string') {
      return {
          id: `zhongte_${id}`,
          type: QuestionType.CHOICE,
          content,
          options: arg3,
          correctAnswer: [arg4],
          category: '中特',
          tags: ['中特', '期末题库']
      };
  }

  // 3. 151-300 Style: (answer: string[], options: string[])
  if (Array.isArray(arg3) && Array.isArray(arg4)) {
      return {
          id: `zhongte_${id}`,
          type: QuestionType.CHOICE,
          content,
          options: arg4, // 4th is options
          correctAnswer: arg3, // 3rd is answer
          category: '中特',
          tags: ['中特', '期末题库']
      };
  }

  // Fallback
  return {
    id: `zhongte_${id}`,
    type: QuestionType.CHOICE,
    content,
    options: Array.isArray(arg4) ? arg4 : undefined,
    correctAnswer: arg3,
    category: '中特',
    tags: ['中特', '期末题库']
  };
};

